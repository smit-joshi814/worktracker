#!/bin/bash
set -e

APP_NAME="WorkHourTracker"
INSTALL_DIR="$HOME/.local/share/$APP_NAME"
BIN_DIR="$HOME/.local/bin"
DESKTOP_DIR="$HOME/.local/share/applications"
AUTOSTART_DIR="$HOME/.config/autostart"
ICON_DIR="$HOME/.local/share/icons"

JAR_NAME="WorkHourTracker.jar"
ICON_NAME="worktracker.png"

echo "Work Hour Tracker Installer"
echo "============================"

# Check for Java
if ! command -v java &> /dev/null; then
    echo "❌ Java not found. Please install Java and try again."
    exit 1
fi

# Check PATH
if [[ ":$PATH:" != *":$BIN_DIR:"* ]]; then
    echo "⚠️ Note: $BIN_DIR is not in your PATH."
    echo "You may want to add the following to your shell profile:"
    echo "export PATH=\"\$PATH:$BIN_DIR\""
fi

# Create directories
mkdir -p "$INSTALL_DIR" "$BIN_DIR" "$DESKTOP_DIR" "$AUTOSTART_DIR" "$ICON_DIR"

# Extract archive
echo "Extracting application files..."

ARCHIVE_LINE=$(awk '/^__ARCHIVE_BELOW__/ {print NR + 1; exit 0; }' "$0")

# Extract JAR
tail -n +$ARCHIVE_LINE "$0" | sed -n '/^###JAR###$/,/^###ENDJAR###$/p' | sed '1d;$d' | base64 -d > "$INSTALL_DIR/$JAR_NAME"

# Extract Icon
tail -n +$ARCHIVE_LINE "$0" | sed -n '/^###ICON###$/,/^###ENDICON###$/p' | sed '1d;$d' | base64 -d > "$ICON_DIR/$ICON_NAME"

# Create launcher script
cat > "$BIN_DIR/worktracker" << EOF
#!/bin/bash
APP_DIR="$INSTALL_DIR"
JAR="\$APP_DIR/$JAR_NAME"
if [ -f "\$JAR" ]; then
    cd "\$APP_DIR"
    java -jar "\$JAR" "\$@"
else
    echo "Work Hour Tracker is not installed correctly."
    exit 1
fi
EOF

chmod +x "$BIN_DIR/worktracker"

# Create desktop entry
cat > "$DESKTOP_DIR/worktracker.desktop" << EOF
[Desktop Entry]
Version=1.0
Type=Application
Name=Work Hour Tracker
Comment=Track your daily working hours and breaks
Exec=$BIN_DIR/worktracker
Icon=$ICON_NAME
Terminal=false
Categories=Office;Utility;
StartupNotify=true
EOF

# Create autostart entry
cp "$DESKTOP_DIR/worktracker.desktop" "$AUTOSTART_DIR/worktracker.desktop"

# Create uninstall script
cat > "$INSTALL_DIR/uninstall.sh" << 'EOF'
#!/bin/bash

APP_NAME="WorkHourTracker"
INSTALL_DIR="$HOME/.local/share/$APP_NAME"
BIN_DIR="$HOME/.local/bin"
DESKTOP_DIR="$HOME/.local/share/applications"
AUTOSTART_DIR="$HOME/.config/autostart"
ICON_DIR="$HOME/.local/share/icons"
ICON_NAME="worktracker.png"

echo "Uninstalling Work Hour Tracker..."
pkill -f "$APP_NAME.jar" 2>/dev/null || true

rm -f "$BIN_DIR/worktracker"
rm -f "$DESKTOP_DIR/worktracker.desktop"
rm -f "$AUTOSTART_DIR/worktracker.desktop"
rm -f "$ICON_DIR/$ICON_NAME"
rm -rf "$INSTALL_DIR"

echo "✅ Work Hour Tracker uninstalled successfully."
EOF

chmod +x "$INSTALL_DIR/uninstall.sh"

echo ""
echo "✅ Installation complete!"
echo "Run from terminal: worktracker"
echo "Uninstall with: $INSTALL_DIR/uninstall.sh"
exit 0

